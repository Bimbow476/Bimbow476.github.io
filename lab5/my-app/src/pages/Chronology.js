const periods = ["Усі", "Стародавній світ", "Середньовіччя", "Новий час", "Новітній час"];

const Chronology = () => {
    const [selectedItem, setSelectedItem] = useState(null);
    const [selectedPeriod, setSelectedPeriod] = useState("Усі");

    const openModal = (item) => setSelectedItem(item);
    const closeModal = () => setSelectedItem(null);

    const filteredData = selectedPeriod === "Усі"
        ? timelineData
        : timelineData.filter((item) => item.period === selectedPeriod);

    return (
        <main>
            <div className="filter-buttons">
                {periods.map((period) => (
                    <button
                        key={period}
                        className={period === selectedPeriod ? "active" : ""}
                        onClick={() => setSelectedPeriod(period)}
                    >
                        {period}
                    </button>
                ))}
            </div>

            <section className="timeline">
                {filteredData.map((item, index) => (
                    <div key={index} className="timeline-item">
                        <div className="timeline-date">{item.year}</div>
                        <div className="timeline-content">
                            <h3>{item.title}</h3>
                            <p>{item.description}</p>
                            <button className="more-info" onClick={() => openModal(item)}>
                                Дізнатися більше
                            </button>
                        </div>
                    </div>
                ))}
            </section>

            {selectedItem && (
                <div className="modal" onClick={closeModal}>
                    <div className="modal-content" onClick={(e) => e.stopPropagation()}>
                        <span className="close" onClick={closeModal}>
                            &times;
                        </span>
                        <h2>{selectedItem.title}</h2>
                        <p>{selectedItem.details}</p>
                    </div>
                </div>
            )}
        </main>
    );
};

export default Chronology;
